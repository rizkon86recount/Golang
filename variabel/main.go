package main

import "fmt"

func main() {
	// Deklarasi variabel dengan tipe data yang harus ditulis
	var nama string
	var umur int
	var tinggi float64
	var mahasiswa bool

	// Inisialisasi variabel
	nama = "Rizkon"
	umur = 21
	tinggi = 170.5
	mahasiswa = true

	// Menampilkan nilai variabel
	fmt.Println("Nama:", nama)
	fmt.Println("Umur:", umur)
	fmt.Println("Tinggi:", tinggi)
	fmt.Println("Mahasiswa:", mahasiswa)
}